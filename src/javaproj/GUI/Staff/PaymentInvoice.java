    private void printActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_printActionPerformed
        // TODO add your handling code here:
        try {
            File file = new File(filename); // keep filename in class field
            PDDocument printDoc = PDDocument.load(file); // load a fresh one just for printing

            PrinterJob job = PrinterJob.getPrinterJob();
            job.setPageable(new PDFPageable(printDoc));
            if (job.printDialog()) {
                job.print();
            }
            printDoc.close(); // close after printing
        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Printing failed:\n" + e.getMessage(),
                                      "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_printActionPerformed

