    public PaymentInvoice(String filename){
        initComponents();
        this.filename = filename;
        
        try {
            document = PDDocument.load(new File(filename));
            PDFRenderer renderer = new PDFRenderer(document);
            BufferedImage pageImage = renderer.renderImageWithDPI(0, 50); 

            // Display image in pdfLocation JPanel
            pdfLocation.removeAll();
            JLabel label = new JLabel(new ImageIcon(pageImage));
            pdfLocation.setLayout(new BorderLayout());
            pdfLocation.add(new JScrollPane(label), BorderLayout.CENTER);
            pdfLocation.revalidate();
            pdfLocation.repaint();
            document.close();
        } catch (IOException ex) {
            ex.printStackTrace();
            javax.swing.JOptionPane.showMessageDialog(this,
                "Failed to load invoice PDF:\n" + ex.getMessage(),
                "Error",
                javax.swing.JOptionPane.ERROR_MESSAGE);
        }
    }

