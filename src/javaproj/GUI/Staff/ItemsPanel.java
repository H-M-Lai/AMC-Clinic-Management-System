    private void updateTableContent(){
        model.setColumnIdentifiers(columnNames);
        type = (String) itemsTypeComboBox.getSelectedItem();
        
        if(type.equals("Medication")){
            items = medications;

        }
        else if (type.equals("Service")){
            items = services;
            }            
        
        Utils.refreshTable(itemsTable,items, x -> new Object[]{
                x.getId(),
                x.getName(),
                x.getDescription(),
                String.format("%.2f", x.getUnitPrice()),
                x.getCategory()
            });
    }
    private void reloadTxt(){
        this.medications = medicationService.medicationList();
        this.services = serviceService.serviceList();
    }
    
    private void applyFilter(){
        final String q = (search.getText() == null ? "" : search.getText().trim().toLowerCase());
        List<Item> filtered = new ArrayList<>();

        List<? extends Item> base =
            "Medication".equalsIgnoreCase(type) ? medications : services;

        for (Item x : base) {
            boolean match = q.isEmpty()
                || x.getId().toLowerCase().contains(q)
                || x.getName().toLowerCase().contains(q)
                || x.getDescription().toLowerCase().contains(q)
                || (x.getCategory() != null && x.getCategory().toLowerCase().contains(q));
            if (match) filtered.add(x);
        }

        Utils.refreshTable(itemsTable, filtered, x -> new Object[] {
            x.getId(),
            x.getName(),
            x.getDescription(),
            String.format("%.2f", x.getUnitPrice()),
            x.getCategory()
        });
        Utils.resizeColumnWidth(itemsTable);
    }
    private void addLiveSearch() {
        search.getDocument().addDocumentListener(new DocumentListener() {
            public void insertUpdate(DocumentEvent e) { applyFilter(); }
            public void removeUpdate(DocumentEvent e) { applyFilter(); }
            public void changedUpdate(DocumentEvent e) { applyFilter(); }
        });
    }
    private DefaultTableModel model = new DefaultTableModel(columnNames, 0){
        @Override
        public boolean isCellEditable(int row, int column) {
            return false;
        }
    };
    public ItemsPanel(PanelController controller) {
        
        this.controller = controller;
        initComponents();
        reloadTxt();
        updateTableContent();
        addLiveSearch();
    }
    
