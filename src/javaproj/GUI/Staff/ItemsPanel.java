    private void deleteBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteBtnActionPerformed
        // TODO add your handling code here:
        int selectedRow = itemsTable.getSelectedRow();
        if (selectedRow == -1) {
            JOptionPane.showMessageDialog(this, "Please select a record to delete.");
            return;
        }

        String selectedId = itemsTable.getValueAt(selectedRow, 0).toString();
        int opt = JOptionPane.showConfirmDialog(this, "Are you sure to delete this record?",
                                                "Delete Item", JOptionPane.YES_NO_OPTION);
        if (opt != JOptionPane.YES_OPTION) return;

        boolean removed;
        if ("Medication".equalsIgnoreCase(type)) {
            removed = medicationService.deleteMedication(selectedId);
        } else {
            removed = serviceService.deleteService(selectedId);
        }

        if (!removed) {
            JOptionPane.showMessageDialog(this, "Record not found (it may have been deleted already).");
            return;
        }
        reloadTxt();
        updateTableContent();
    }//GEN-LAST:event_deleteBtnActionPerformed

