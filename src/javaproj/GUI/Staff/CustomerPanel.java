    private void applyFilter(){
        if (customers == null) return;
        List<Customer> filtered = customerService
                .search(customers, searchField.getText())
                .stream()
                .map(u -> (Customer) u)
                .toList();

        Utils.refreshTable(customerTable, filtered, c -> new Object[] {
            c.getSystemId(), c.getName(), c.getIdentityNumber(),
            c.getAddress(), c.getPhone(), c.getEmail()
        });
    }
    
    private void addLiveSearch() {
        searchField.getDocument().addDocumentListener(new DocumentListener() {
            public void insertUpdate(DocumentEvent e) { applyFilter(); }
            public void removeUpdate(DocumentEvent e) { applyFilter(); }
            public void changedUpdate(DocumentEvent e) { applyFilter(); }
        });
    }
    
    private void reloadTxt(){
        customers = customerService.customers();
    }
    private void reloadAndRefresh() {
    // Make sure the maps and list are up to date
        reloadTxt();
        // Render using refreshTable
        Utils.refreshTable(customerTable,customers,c -> new Object[] {
            c.getSystemId(),
            c.getName(),
            c.getIdentityNumber(),
            c.getAddress(),
            c.getPhone(),
            c.getEmail()
            }
        );
        }
