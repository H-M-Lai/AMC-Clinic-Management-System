    private void editBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_editBtnActionPerformed
        // TODO add your handling code here:
        int selectedRow = customerTable.getSelectedRow();
        if (selectedRow == -1){
            JOptionPane.showMessageDialog(this,"Please select a record to edit.");
            return;
        }
        String id = customerTable.getValueAt(selectedRow,0).toString();
        CreateAndEditCustomer edit = new CreateAndEditCustomer(id);
        edit.addWindowListener(new java.awt.event.WindowAdapter() {
            @Override
            public void windowClosed(java.awt.event.WindowEvent e) {
                reloadAndRefresh();
            }
        });
        edit.setVisible(true);
    }//GEN-LAST:event_editBtnActionPerformed

    private void deleteBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteBtnActionPerformed
        // TODO add your handling code here:
        int selectedRow = customerTable.getSelectedRow();
        if (selectedRow == -1){
            JOptionPane.showMessageDialog(this,"Please select a record to delete.");
            return;
        }

        String id = customerTable.getValueAt(selectedRow,0).toString();
        int option = JOptionPane.showConfirmDialog(
                this, "Are you sure to delete this record?", "Delete Customer", JOptionPane.YES_NO_OPTION);
        if(option != JOptionPane.YES_OPTION) return;

        boolean removed = customerService.deleteCustomer(id);
        if(!removed){
            JOptionPane.showMessageDialog(this, "Record not found (it may have been deleted already).");
            return;
        }
        reloadAndRefresh();
    }//GEN-LAST:event_deleteBtnActionPerformed

