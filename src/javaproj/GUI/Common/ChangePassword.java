    private void editBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_editBtnActionPerformed
        // TODO add your handling code here:
        String currentPw   = new String(password0.getPassword()).trim();
        String newPw       = new String(password1.getPassword()).trim();
        String reconfirmPw = new String(password2.getPassword()).trim();

        User current = Session.getCurrentUser();
        if (current == null) {
            JOptionPane.showMessageDialog(this, "No user is logged in.", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        boolean ok = changePasswordFor(current, currentPw, newPw, reconfirmPw);
        if (!ok) {
            JOptionPane.showMessageDialog(this, "Password change failed! Check your current password and confirmation.");
        } else {
            JOptionPane.showMessageDialog(this, "Password changed successfully!");
            this.dispose();
        }
    }//GEN-LAST:event_editBtnActionPerformed

