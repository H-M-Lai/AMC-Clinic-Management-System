    private boolean changePasswordFor(User user, String currentPw, String newPw, String reconfirmPw) {
        if (user instanceof Customer) return customerSvc.changePassword(user, currentPw, newPw, reconfirmPw);
        if (user instanceof Doctor) return doctorSvc.changePassword(user, currentPw, newPw, reconfirmPw);
        if (user instanceof Staff) return staffSvc.changePassword(user, currentPw, newPw, reconfirmPw);
        if (user instanceof Supermanager) return superSvc.changePassword(user, currentPw, newPw, reconfirmPw);
        if (user instanceof Manager) return managerSvc.changePassword(user, currentPw, newPw, reconfirmPw);
        return false;
    }

