    private void cancelAppointment(){
        String id = AppointmentIDSelection.getSelectedItem().toString();
        String doctor = doctorField.getText();
        if(id.equals("Select Appointment")){ 
            JOptionPane.showMessageDialog(this, "Please select a record to cancel.", "Done", JOptionPane.INFORMATION_MESSAGE);
            return;
        }
        
        int confirm = JOptionPane.showConfirmDialog(
                this,
                "Cancel appointment " + id + " with Doctor " + doctor + "?",
                "Confirm Cancel",
                JOptionPane.YES_NO_OPTION
        );
        if (confirm != JOptionPane.YES_OPTION) {
            JOptionPane.showMessageDialog(this,
                    "Appointment not cancelled.",
                    "Info", JOptionPane.ERROR_MESSAGE);
            return;
        } 
        //model.setValueAt("Cancelled", row, 4);
        try {
            customerAppt.cancelAppointment(id);
            JOptionPane.showMessageDialog(this, "Appointment " + id + " cancelled successfully.", "Done", JOptionPane.INFORMATION_MESSAGE);

            // refresh the dropdown and the table to reflect the cancellation
            loadAppointmentsAndFilters();
            setupTable();
            updateField();

        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "Cancellation unsuccessful: " + ex.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    }
    private String doctorNameById(String doctorId) {
        for (Doctor d : doctorService.doctors()) {
            if (d.getSystemId().equals(doctorId)) return d.getName();
        }
        return "(unknown)";
    }
