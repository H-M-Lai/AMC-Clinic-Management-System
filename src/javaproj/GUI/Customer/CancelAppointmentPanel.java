    private void makeTableReadOnly() {
        AppoinmentsRecordTable.setDefaultEditor(Object.class, null);
    }
    
    private void loadAppointmentsAndFilters() {
        List<String> appointmentList = new ArrayList<>();
        appointmentList.add("Select Appointment");
        
        List<Appointment> list = customerAppt.myAppointments(Session.getCurrentUser().getSystemId());
        for (Appointment a : list) {
            if (a.getStatus() == Appointment.Status.SCHEDULED) {
                appointmentList.add(a.getAppointmentId());
            }
        }
        Utils.updateComboBox(AppointmentIDSelection, appointmentList);
    }
    private void updateField(){
        String id = AppointmentIDSelection.getSelectedItem().toString();
        if(id.equals("Select Appointment")){
            doctorField.setText("---");
            typeField.setText("---");
            dateField.setText("---");
            timeField.setText("---");
        }
        else{
            Appointment selected = customerAppt.getAppointment(id);
            doctorField.setText(doctorNameById(selected.getDoctorId()));
            typeField.setText(selected.getType().toString());
            dateField.setText(selected.getDate());
            timeField.setText(selected.getTime());
        }
    }
    private void setupTable(){
        List<Appointment> appointments = customerAppt.myAppointments(Session.getCurrentUser().getSystemId());
        List<Appointment> filter = new ArrayList<>();
        for(Appointment a: appointments){
            if(a.getStatus().equals(Appointment.Status.SCHEDULED)){
                filter.add(a);
            }
        }
        Utils.refreshTable(AppoinmentsRecordTable,filter,a -> new Object[] {
                a.getAppointmentId(),
                a.getDate(),
                a.getTime(),
                doctorNameById(a.getDoctorId()),
                a.getStatus()
            }
        );
    }

